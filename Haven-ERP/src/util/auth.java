package util;

import bean.Login;

public class auth {
	private boolean flag;
	private Login login;
	
	
	
	
	
	
	public void setFlag(boolean flag) {
		this.flag = flag;
	}





	public void setLogin(Login login) {
		this.login = login;
	}





	public boolean isFlag() {
		return flag;
	}
	public Login getLogin() {
		return login;
	}
	
	

}
